//aliases
//variable
type vartype = string | number;
let variable: vartype = 10;
variable = "Sai";
console.log(variable);

//array
type arrtype = string | boolean | number;
let arr: arrtype[] = [];
arr.push("Sai");
arr.push(2);
arr.push(true);
console.log(arr);

//Object
type objtype = { Name: string, Rollno: 25 };
let obj: objtype = { Name: "Sai", Rollno: 25 };
console.log(obj);


